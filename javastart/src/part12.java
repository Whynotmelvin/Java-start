public class part12 {
    public static void main(String[] args) {
        // Switch statement
        int n = 7;
        switch (n) {
            case 1:
                System.out.println("The new value is 1");
                break;
            case 2:
                System.out.println("This is value is 2 ");
                break;

            default:
                System.out.println("Your value is nonsense");
                break;
        }
    }
}
