public class Part8 {
    public static void main (String [] args){

        double result ;
        // Raise to the power of 
        result = Math.pow(2,3);

        //Absolute value 
        result = Math.abs(-5);

        // Square root 
        result = Math.sqrt(9);

        // Round a number 
        result = Math.round(3.342);

        // Round up no matter what 
        result = Math.ceil(3.14);

        //Round down no matter what
        result = Math.floor(5.98);

        // Maximum values between 2 values
        result = Math.max(10,20);

        // Minimum values between 2 values
        result = Math.min(10, 20);

        System.out.println(result);

        // Value of Pie
        System.out.println(Math.PI);

        // Exponential constant and Eulers constant 
        System.out.println(Math.E);

    }
}
